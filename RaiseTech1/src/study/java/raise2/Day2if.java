package study.java.raise2;

class Day2if {
	 public static void main(String[] args) {
		    numbers (0);
		    numbers (1);
	 }
	 public static void numbers(int number) {
		    // 引数が0なら偽、1なら真、どちらでもないときの条件分岐を追加してください
		    	if (number == 0) {
		    		System.out.println("偽");
		    	} else if (number ==1) {
		    		System.out.println("真");
		    	} else {
		    		System.out.println("異");
		    	}
     }

}
